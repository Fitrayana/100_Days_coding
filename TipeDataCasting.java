public class Casting {
    public static void main(String[] args) {
        
        double doubleNilai = 10.5;
        int intNilai = (int) doubleNilai;

        System.out.println("Double Nilai: " + doubleNilai);
        System.out.println("Integer Nilai: " + intNilai);
    }
}

//Tipe data casting merupakan tipe data non primitif yang dimana ia merupakan suatu proses mengubah nilai dari satu tipe data ke tipe data lainnya.
