//Berikut contoh penggunaan tipe data Byte
//Tipe data byte digunakan untuk menyimpan bilangan bulat yang  kecil

public class Byte {
    public static void main(String[] args) {
        byte nilaiByte = 40; // Mendeklarasikan variabel byte dengan nilai 40
        System.out.println("Nilai byte: " + nilaiByte);
        
        // contoh operasi aritmatika dengan tipe data byte
        byte nilai1 = 10;
        byte nilai2 = 20;
        byte hasil = (byte) (nilai1 + nilai2);
        System.out.println("Hasil penjumlahan: " + hasil);
    }
}
